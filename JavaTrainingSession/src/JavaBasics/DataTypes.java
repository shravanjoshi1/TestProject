package JavaBasics;

public class DataTypes {

	public static void main(String[] args) {
		
	//integer , Char, Double, Boolean are primitive data types	
		// 1. int
		int i = 10;
		i =20;
		int j =30;
		int k = -40;
		int p =0;	
		System.out.println(i);
		
		//2. double
		double d = 12.33;
		double d1 = 34.44;
		double d2 = 100; //100.00
		
		//3.Char ; single digit value . Should be written within single quotes
		char c = 'a';
		char c1 = 'A';
		char c2 = '1';
		char c3 = '$';
		
		//4.Boolean
		boolean b1 = true;
		boolean b2 = false;
		
		//5. String is a class but not a data type
		
		String S = "Shravan";
		String S1 = "Selenium Automation";
		String S2 = "Hello World!";
		String S3 = "1000";

	}

}
