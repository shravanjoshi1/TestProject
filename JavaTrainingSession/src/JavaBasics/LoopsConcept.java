package JavaBasics;

public class LoopsConcept {

	public static void main(String[] args) {
		
		//1. While loop
		
		int i = 1;
		while (i<= 100) {
			System.out.println(i);
			i= i+1;
			
		
		}
		
		//2 for loop
		
		
	for(int x=10; x>=1;x--) {
		System.out.println(x);
	}
		
		
	}
}
		


